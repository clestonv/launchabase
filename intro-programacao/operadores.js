/*================================
OPERADORES DE COMPARAÇÀO

> Maior
< Menor
>= Maior igual a
<= Menor igual a
== Igual a
=== Igual e do mesmo tipo
!= Diferente de
!== Diferente, inclusive do tipo

==================================*/

console.log(5 > 4) // Cinco maior que 4? true
console.log(5 < 4) // Cinco menor que 4? false
console.log(5 >= 4) // Cindo maior igual a 4? true
console.log(4 <= 4) // Quatro menor igual a 4? true

console.log(4 == '4') // Quatro igual a 4? true
console.log( 4 === '4') // Quatro igual e do mesmo tipo que 4? false
console.log( 4 != '5') // Quatro é diferente de 5? true
console.log( 4 !== '5') // Quatro é diferente, inclusive do tipo de 5? true

